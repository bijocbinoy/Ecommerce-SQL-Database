BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "Customers" (
	"customer_id"	INTEGER,
	"name"	TEXT NOT NULL,
	"email"	TEXT UNIQUE,
	"gender"	TEXT CHECK("gender" IN ('Male', 'Female', 'Other')),
	"age"	INTEGER CHECK("age" >= 18),
	"loyalty_level"	TEXT CHECK("loyalty_level" IN ('Bronze', 'Silver', 'Gold', 'Platinum')),
	"country"	TEXT,
	PRIMARY KEY("customer_id")
);
CREATE TABLE IF NOT EXISTS "OrderItems" (
	"order_id"	INTEGER,
	"product_id"	INTEGER,
	"quantity"	INTEGER CHECK("quantity" > 0),
	"unit_price"	REAL CHECK("unit_price" >= 0),
	PRIMARY KEY("order_id","product_id"),
	FOREIGN KEY("order_id") REFERENCES "Orders"("order_id"),
	FOREIGN KEY("product_id") REFERENCES "Products"("product_id")
);
CREATE TABLE IF NOT EXISTS "Orders" (
	"order_id"	INTEGER,
	"customer_id"	INTEGER,
	"order_date"	TEXT,
	"total_amount"	REAL CHECK("total_amount" >= 0),
	"payment_method"	TEXT CHECK("payment_method" IN ('Credit Card', 'Debit Card', 'PayPal', 'Gift Card')),
	"status"	TEXT CHECK("status" IN ('Pending', 'Shipped', 'Delivered', 'Cancelled')),
	"satisfaction_rating"	TEXT CHECK("satisfaction_rating" IN ('Low', 'Medium', 'High')),
	PRIMARY KEY("order_id"),
	FOREIGN KEY("customer_id") REFERENCES "Customers"("customer_id")
);
CREATE TABLE IF NOT EXISTS "Products" (
	"product_id"	INTEGER,
	"product_name"	TEXT NOT NULL,
	"category"	TEXT CHECK("category" IN ('Electronics', 'Clothing', 'Books', 'Home', 'Beauty', 'Toys')),
	"price"	REAL CHECK("price" > 0),
	"stock_level"	INTEGER CHECK("stock_level" >= 0),
	PRIMARY KEY("product_id")
);
CREATE TABLE IF NOT EXISTS "Reviews" (
	"review_id"	INTEGER,
	"product_id"	INTEGER,
	"customer_id"	INTEGER,
	"rating"	INTEGER CHECK("rating" BETWEEN 1 AND 5),
	"comment"	TEXT,
	"review_date"	TEXT,
	PRIMARY KEY("review_id"),
	FOREIGN KEY("customer_id") REFERENCES "Customers"("customer_id"),
	FOREIGN KEY("product_id") REFERENCES "Products"("product_id")
);
